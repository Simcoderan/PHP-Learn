<?php
// switch-case example
$day = "Wednesday";

switch ($day) {
    case "Monday":
        echo "Start of the week<br>";
        break;
    case "Wednesday":
        echo "Midweek day<br>";
        break;
    case "Friday":
        echo "Weekend starts<br>";
        break;
    default:
        echo "Regular day<br>";
}
?>
