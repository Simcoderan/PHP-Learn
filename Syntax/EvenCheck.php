<?php
// for-loop.php
// This file demonstrates the use of a for loop in PHP.

// The for loop executes the block of code a specified number of times.
for ($i = 1; $i <= 5; $i++) {
    echo "Iteration number: $i<br>";
}

// Explanation:
// $i = 1 : initialization (starting point)
// $i <= 5 : condition (loop runs while this is true)
// $i++ : increment (runs after each iteration)
?>
